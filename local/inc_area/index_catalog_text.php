<h2 class="h1 text-align-center">Каталог</h2>
<div class="p text text-align-center">
	<p>
		 На территории 10 гектаров садового центра Imperial Garden представлены растения из ведущих питомников России, Германии, Польши и Голландии. Более 10 000 видов и сортов: хвойные и лиственные породы крупномеров, бонсаи и стриженные формы растений, плодовые деревья, декоративные кустарники и травянистые многолетники.
