<p>
	 Imperial Garden – это садовый курорт и центр загородных развлечений на Новой Риге. Это самый красивый в России и один из самых больших садовых центров в Европе.
</p>
<p>
	 Пешие прогулки по живописным аллеям, семейные обеды в уютном кафе, вкуснейшее какао ручной работы на шоколадной фабрике, приветливые альпаки, еноты, кролики и величественные пеликаны из собственного зоопарка – всё вместе составляет искусную мозаику наслаждений отдыха на природе.&nbsp;
</p>