<p>
	 Imperial Garden – не стандартный садовый центр. На нашей территории мы постарались воплотить идеальный образ загородного стиля жизни.
</p>
<p>
	 Здесь вы найдете не только выставочное садовое пространство, наполненное разнообразными видами растений, за которыми мы с любовью ухаживаем, но и настоящий комплекс для семейного отдыха.
</p>
<p>
	 Выпейте чашечку кофе, пока дети играют рядом на современной оборудованной площадке; посетите зоопарк, где живут альпаки, пеликаны и павлины; насладитесь качественным шоколадом собственного производства от фабрики Vento d’Oro; вдохновитесь силой искусства, посетив Art Gallery и прогулявшись по саду Revive, отмеченному на выставке садово-паркового искусства в Англии.
</p>
